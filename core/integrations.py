@dataclass
class WebhookResult:
    success: bool
    status_code: Optional[int] = None
    response_body: str = ""
    error: str = ""


def post_webhook(
    url: str,
    payload: Dict,
    headers: Optional[Dict[str, str]] = None,
    timeout: int = 10,
) -> WebhookResult:
    """
    POST a JSON payload to the given URL.
    Returns a WebhookResult with success flag and response info.
    No external dependencies — uses urllib.
    """
    if not url or not url.startswith(("http://", "https://")):
        return WebhookResult(success=False, error="Invalid URL — must start with http:// or https://")

    try:
        body = json.dumps(payload).encode("utf-8")
        req_headers = {"Content-Type": "application/json", "User-Agent": "ProductSkout/1.0"}
        if headers:
            req_headers.update(headers)

        req = urllib.request.Request(url, data=body, headers=req_headers, method="POST")
        with urllib.request.urlopen(req, timeout=timeout) as resp:
            resp_body = resp.read().decode("utf-8", errors="replace")[:500]
            return WebhookResult(success=True, status_code=resp.status, response_body=resp_body)

    except urllib.error.HTTPError as e:
        return WebhookResult(success=False, status_code=e.code, error=str(e))
    except urllib.error.URLError as e:
        return WebhookResult(success=False, error=f"Connection error: {e.reason}")
    except Exception as e:
        return WebhookResult(success=False, error=str(e))


def build_webhook_payload(card_dict: Dict, extra_fields: Optional[Dict] = None) -> Dict:
    """Build a clean webhook payload from an idea card dict."""
    payload = {
        "event":       "skout.idea_card.generated",
        "timestamp":   date.today().isoformat(),
        "idea": {
            "title":           card_dict.get("title", ""),
            "domain":          card_dict.get("domain", ""),
            "score":           card_dict.get("verdict_score", 0),
            "band":            card_dict.get("verdict_label", ""),
            "origin":          card_dict.get("origin", ""),
            "research_status": card_dict.get("research_status", ""),
        },
    }
    if extra_fields:
        payload.update(extra_fields)
    return payload


