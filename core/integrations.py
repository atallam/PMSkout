def to_jira_issue(card_dict: Dict, project_key: str = "SC") -> Dict:
    """
    Generate a Jira issue creation payload (REST API v3 format).
    POST to: /rest/api/3/issue
    """
    title  = card_dict.get("title", "Untitled Idea")
    score  = card_dict.get("verdict_score", 0)
    domain = card_dict.get("domain", "")
    band   = card_dict.get("verdict_label", "")

    description_text = "\n\n".join([
        f"*Problem:* {card_dict.get('problem', '')}",
        f"*Primary User:* {card_dict.get('primary_stakeholder', '')}",
        f"*Current State:* {card_dict.get('current_state', '')}",
        f"*Proposed Direction:* {card_dict.get('proposed_direction', 'TBD')}",
        f"*Hypothesis:* {card_dict.get('hypothesis', '')}",
        "",
        "*Validated Evidence:*",
        "\n".join(f"- {e}" for e in (card_dict.get("validated_evidence") or [])) or "_None yet_",
        "",
        "*Key Adoption Risk:*",
        card_dict.get("key_adoption_risk", ""),
        "",
        "*Next Actions:*",
        "\n".join(f"- {a}" for a in (card_dict.get("next_actions") or [])),
        "",
        f"_Skout score: {int(score)}/100 — {band} — {domain}_",
    ])

    priority = (
        "Highest" if score >= 80 else
        "High"    if score >= 60 else
        "Medium"  if score >= 40 else
        "Low"
    )

    return {
        "fields": {
            "project":     {"key": project_key},
            "summary":     f"[Skout] {title} (score: {int(score)}/100)",
            "description": {
                "type":    "doc",
                "version": 1,
                "content": [
                    {
                        "type": "paragraph",
                        "content": [{"type": "text", "text": description_text}]
                    }
                ],
            },
            "issuetype":   {"name": "Story"},
            "priority":    {"name": priority},
            "labels":      ["skout", f"domain-{domain}", f"band-{card_dict.get('verdict_label','').lower().replace(' ','-')}"],
        }
    }


def to_jira_json_str(card_dict: Dict, project_key: str = "SC") -> str:
    return json.dumps(to_jira_issue(card_dict, project_key), indent=2)


