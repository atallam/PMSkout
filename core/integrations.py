def to_notion_markdown(card_dict: Dict) -> str:
    """
    Generate a Notion-compatible markdown representation of an IdeaCard.
    Notion imports markdown with its own block format.
    """
    title  = card_dict.get("title", "Untitled Idea")
    score  = card_dict.get("verdict_score", 0)
    domain = card_dict.get("domain", "")
    date_  = card_dict.get("date_created", str(date.today()))
    emoji  = card_dict.get("verdict_emoji", "")

    lines = [
        f"# {emoji} {title}",
        f"> **Domain:** {domain}  |  **Score:** {int(score)}/100  |  **Date:** {date_}",
        "",
        "---",
        "",
        "## 📌 Problem",
        card_dict.get("problem", ""),
        "",
        "## 👤 Primary User",
        card_dict.get("primary_stakeholder", ""),
        "",
        "## 🗺️ Current State",
        card_dict.get("current_state", ""),
        "",
        "## 💡 Proposed Direction",
        card_dict.get("proposed_direction", "_Not yet defined_"),
        "",
        "---",
        "",
        "## 🔬 Hypothesis",
        f"> {card_dict.get('hypothesis', '')}",
        "",
        "---",
        "",
        "## ✅ Validated Evidence",
    ]
    for e in (card_dict.get("validated_evidence") or []):
        lines.append(f"- {e}")
    if not card_dict.get("validated_evidence"):
        lines.append("_No findings logged yet_")

    lines += [
        "",
        "## ⚠️ Key Adoption Risk",
        card_dict.get("key_adoption_risk", ""),
        "",
        "## ❓ Open Questions",
    ]
    for q in (card_dict.get("open_questions") or []):
        if q.strip():
            lines.append(f"- {q}")

    lines += [
        "",
        "## ▶️ Next Actions",
    ]
    for a in (card_dict.get("next_actions") or []):
        lines.append(f"- [ ] {a}")

    lines += [
        "",
        "---",
        f"_Generated by Product Skout · Supply Chain Edition_",
    ]
    return "\n".join(lines)


