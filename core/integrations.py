def to_csv(card_dicts: List[Dict]) -> str:
    """
    Convert a list of idea card dicts to CSV string.
    Works for both a single card (list of 1) or a portfolio.
    """
    if not card_dicts:
        return ""

    fieldnames = [
        "title", "domain", "verdict_score", "verdict_label",
        "date_created", "origin", "primary_stakeholder",
        "current_state", "research_status", "proposed_direction",
    ]

    output = io.StringIO()
    writer = csv.DictWriter(output, fieldnames=fieldnames, extrasaction="ignore")
    writer.writeheader()
    for d in card_dicts:
        writer.writerow({k: d.get(k, "") for k in fieldnames})
    return output.getvalue()


