TEAM_IDEAS_PATH = str(Path(__file__).parent.parent / "data" / "team_ideas.json")


def _ensure_team_file(path: str = TEAM_IDEAS_PATH):
    p = Path(path)
    p.parent.mkdir(parents=True, exist_ok=True)
    if not p.exists():
        p.write_text("[]", encoding="utf-8")


def get_team_ideas(team_id: str = "default", path: str = TEAM_IDEAS_PATH) -> List[Dict]:
    """Load shared team ideas for the given team_id."""
    _ensure_team_file(path)
    try:
        with open(path, "r", encoding="utf-8") as f:
            all_ideas = json.load(f)
        return [i for i in all_ideas if i.get("team_id", "default") == team_id]
    except (FileNotFoundError, json.JSONDecodeError):
        return []


def share_to_team(card_dict: Dict, author_name: str, team_id: str = "default", path: str = TEAM_IDEAS_PATH) -> bool:
    """
    Add an idea card to the shared team pool.
    Returns True if added, False if already shared (by title + team_id).
    """
    _ensure_team_file(path)
    try:
        with open(path, "r", encoding="utf-8") as f:
            all_ideas = json.load(f)
    except (FileNotFoundError, json.JSONDecodeError):
        all_ideas = []

    # Dedup check
    existing_titles = {
        i.get("title", "").strip().lower()
        for i in all_ideas if i.get("team_id") == team_id
    }
    if card_dict.get("title", "").strip().lower() in existing_titles:
        return False

    entry = {
        "team_id":      team_id,
        "author":       author_name,
        "shared_date":  date.today().isoformat(),
        "title":        card_dict.get("title", ""),
        "domain":       card_dict.get("domain", ""),
        "score":        card_dict.get("verdict_score", 0),
        "band":         card_dict.get("verdict_label", ""),
        "hypothesis":   card_dict.get("hypothesis", ""),
        "next_actions": card_dict.get("next_actions", []),
        "research_status": card_dict.get("research_status", "not_started"),
        "comments":     [],
    }
    all_ideas.append(entry)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(all_ideas, f, indent=2, ensure_ascii=False)
    return True


def add_team_comment(idea_title: str, author: str, comment: str, team_id: str = "default", path: str = TEAM_IDEAS_PATH) -> bool:
    """Add a comment to a shared team idea."""
    _ensure_team_file(path)
    try:
        with open(path, "r", encoding="utf-8") as f:
            all_ideas = json.load(f)
    except (FileNotFoundError, json.JSONDecodeError):
        return False

    for idea in all_ideas:
        if (idea.get("team_id") == team_id and
                idea.get("title", "").strip().lower() == idea_title.strip().lower()):
            idea.setdefault("comments", []).append({
                "author":  author,
                "text":    comment[:500],
                "date":    date.today().isoformat(),
            })
            with open(path, "w", encoding="utf-8") as f:
                json.dump(all_ideas, f, indent=2, ensure_ascii=False)
            return True
    return False
